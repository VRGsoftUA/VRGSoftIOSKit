#import "UIView+PSSizes.h"

@implementation UIView (PSSizes)

- (CGFloat)left {
    return self.frame.origin.x;
}

- (void)setLeft:(CGFloat)x {
    CGRect frame = self.frame;
    frame.origin.x = x;
    self.frame = frame;
}

- (CGFloat)top {
    return self.frame.origin.y;
}

- (void)setTop:(CGFloat)y {
    CGRect frame = self.frame;
    frame.origin.y = y;
    self.frame = frame;
}

- (CGFloat)right {
    return self.frame.origin.x + self.frame.size.width;
}

- (void)setRight:(CGFloat)right {
    CGRect frame = self.frame;
    frame.origin.x = right - frame.size.width;
    self.frame = frame;
}

- (CGFloat)bottom {
    return self.frame.origin.y + self.frame.size.height;
}

- (void)setBottom:(CGFloat)bottom {
    CGRect frame = self.frame;
    frame.origin.y = bottom - frame.size.height;
    self.frame = frame;
}

- (CGFloat)width {
    return self.frame.size.width;
}

- (void)setWidth:(CGFloat)width {
    CGRect frame = self.frame;
    frame.size.width = width;
    self.frame = frame;
}

- (CGFloat)height {
    return self.frame.size.height;
}

- (void)setHeight:(CGFloat)height {
    CGRect frame = self.frame;
    frame.size.height = height;
    self.frame = frame;
}

- (CGPoint)origin {
    return self.frame.origin;
}

- (void)setOrigin:(CGPoint)origin {
    CGRect frame = self.frame;
    frame.origin = origin;
    self.frame = frame;
}

- (CGSize)size {
    return self.frame.size;
}

- (void)setSize:(CGSize)size {
    CGRect frame = self.frame;
    frame.size = size;
    self.frame = frame;
}

- (CGFloat)rightMargin {
    if (self.superview)
    {
        return self.superview.width - self.frame.size.width - self.frame.origin.x;
    }
    return 0;
}

- (void)setRightMargin:(CGFloat)right {
    
    if (self.superview)
    {
        CGFloat sWidth = self.superview.frame.size.width;
        
        CGRect frame = self.frame;
        frame.origin.x = sWidth - right - frame.size.width;
        self.frame = frame;
    }
}

- (CGFloat)bottomMargin {
    if (self.superview)
    {
        return self.superview.height - self.frame.size.height - self.frame.origin.y;
    }
    return 0;
}

- (void)setBottomMargin:(CGFloat)bottom {
    if (self.superview)
    {
        CGFloat sHeight = self.superview.frame.size.height;
        CGRect frame = self.frame;
        frame.origin.y = sHeight - bottom - frame.size.height;
        self.frame = frame;
    }
}

@end
